import math

#if we only keep the return, it will now show itself in the console
def add(a,b):
    return a + b

def substract(a,b):
    return a - b

def multiply(a,b):
    return a * b

def divide(a,b):
    return a / b

def exp(a,b):
    return math.pow(a,b)

print(add(5,6))
print(substract(120,20))
print(multiply(2,4))
print(divide(9,3))
print(exp(5,5))
